class Survey {
  /// Creates the employee class with required details.
  Survey(this.id, this.name, this.dateCreated, this.questions, this.answers, this.score);

  /// Id of an employee.
  final int id;

  /// Name of an employee.
  final String name;

  final String dateCreated;

  final int questions;

  /// Salary of an employee.
  final int answers;

  /// Salary of an employee.
  final int score;
}